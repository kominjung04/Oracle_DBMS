SELECT * FROM EMP;
--1번
SELECT DEPTNO,ROUND(AVG(SAL)),MAX(SAL),MIN(SAL),COUNT(*)
FROM EMP
GROUP BY DEPTNO
ORDER BY COUNT(DEPTNO);

--2번
SELECT JOB,COUNT(*)
FROM EMP
GROUP BY JOB
HAVING COUNT(*)>=3;

--3번
SELECT TO_CHAR(HIREDATE,'YYYY') HIRE_YEAR,DEPTNO,COUNT(*) CNT
FROM EMP
GROUP BY TO_CHAR(HIREDATE,'YYYY'),DEPTNO;

--4번
SELECT NVL2(COMM,'O','X') EXIST_COMM,COUNT(*) CNT
FROM EMP
GROUP BY NVL2(COMM,'O','X');

--5번
SELECT DEPTNO,TO_CHAR(HIREDATE,'YYYY') HIRE_YEAR,COUNT(*),MAX(SAL),SUM(SAL),AVG(SAL)
FROM EMP
GROUP BY ROLLUP(DEPTNO,TO_CHAR(HIREDATE,'YYYY'))
ORDER BY DEPTNO;

