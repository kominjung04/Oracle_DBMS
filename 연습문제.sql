SELECT * FROM STUDENT;
SELECT * FROM PROFESSOR;
SELECT * FROM HAKJUM;
SELECT * FROM SCORE;

SELECT S.NAME 학생이름,P.NAME 교수명 ,J.TOTAL 점수,H.GRADE 학점
FROM STUDENT S ,PROFESSOR P,HAKJUM H,SCORE J
WHERE S.PROFNO= P.PROFNO(+) 
  AND S.STUDNO = J.STUDNO(+)
  AND J.TOTAL BETWEEN MIN_POINT(+) AND MAX_POINT(+);
  
SELECT S.NAME 학생이름,P.NAME 교수명 ,J.TOTAL 점수,H.GRADE 학점
FROM STUDENT S LEFT OUTER JOIN PROFESSOR P ON (S.PROFNO = P.PROFNO)
LEFT OUTER JOIN SCORE J ON (S.STUDNO = J.STUDNO)
LEFT OUTER JOIN HAKJUM H ON (J.TOTAL BETWEEN H.MIN_POINT AND H.MAX_POINT);