SELECT E.DEPTNO,D.DNAME,E.EMPNO,E.ENAME,E.SAL
FROM EMP E,DEPT D
WHERE E.DEPTNO = D.DEPTNO AND SAL>=2000
ORDER BY E.DEPTNO;

--2번
SELECT E.DEPTNO,D.DNAME,ROUND(AVG(SAL)),MAX(SAL),MIN(SAL),COUNT(*)
FROM EMP E,DEPT D
WHERE E.DEPTNO = D.DEPTNO
GROUP BY E.DEPTNO,D.DNAME
ORDER BY E.DEPTNO;

