SELECT D.DEPTNO, D.DNAME,E1.EMPNO, E1.ENAME, E1.MGR, E1.SAL, E1.DEPTNO, 
       S.LOSAL,S.HISAL, S.GRADE,E2.MGR MGR_EMPNO, E2.MGR MGR_ENAME
FROM  EMP E1 RIGHT OUTER JOIN DEPT D ON(E1.DEPTNO = D.DEPTNO)
      LEFT OUTER JOIN EMP E2 ON(E1.MGR=E2.EMPNO)
      LEFT OUTER JOIN SALGRADE S ON(E1.SAL BETWEEN S.LOSAL AND S.HISAL)
ORDER BY D.DEPTNO,E1.ENAME;

--학생명,지도교수명,성적,학점을 출력하라
